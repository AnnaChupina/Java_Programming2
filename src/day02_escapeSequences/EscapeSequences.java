package day02_escapeSequences;

public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println("Hello students\nToday we have Java class ");
        System.out.println("_________________________________________");
        System.out.println("\tJava programming language");
        System.out.println("\t\tJava");
        System.out.println("_________________________________________");
        System.out.println("\\");
        System.out.println("_________________________________________");
        System.out.println("My favorite TV show is \"The wolking dead\"");

    }

}
/*

    1. \n: new line
    2. \t: paragraph space
    3. \\: back slash
    4.\*: double quote

 */